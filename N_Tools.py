def numpy_to_latex(
    arr: np.ndarray,
    sigfigs: int = 4,
    max_rows: int = -1, # -1 automatically calculates
    max_cols: int = -1, # -1 automatically calculates
    tail_rows: int = 2,
    tail_cols: int = 2,
    make_table: bool = False,
    colnames: list[str] = []
) -> str:
    if not isinstance(arr, np.ndarray):
        raise TypeError("Input must be a numpy.ndarray")

    if arr.ndim >= 3:
        raise ValueError("Array must be 1D or 2D")

    # Treat 1D arrays as column vectors
    if arr.ndim == 1:
        arr = as_col(arr)

    n_rows, n_cols = arr.shape
    
    # If automatically calculating max_rows or max_cols, do so here
    def display_size(
        n,
        full_threshold=8,
        max_display=14,
        scale=0.03):
        if n <= full_threshold:
            return n
        return min(max_display, int(full_threshold + scale * (n - full_threshold)))
    
    if max_rows == -1:
        max_rows = display_size(n_rows)
    if max_cols == -1:
        max_cols = display_size(n_cols)

    if tail_rows < 0 or tail_cols < 0:
        raise ValueError("tail_rows and tail_cols must be nonnegative")

    def format_entry(x):
        try:
            x = float(x)
            return f"{x:.{sigfigs}g}"
        except (ValueError, TypeError):
            return str(x)

    def indices(n, max_n, tail_n):
        if n <= max_n:
            return list(range(n)), False

        if tail_n + 1 >= max_n:
            raise ValueError("tail_n is too large relative to max_n")

        head_n = max_n - tail_n - 1
        idx = list(range(head_n)) + list(range(n - tail_n, n))
        return idx, True

    row_idx, row_trunc = indices(n_rows, max_rows, tail_rows)
    col_idx, col_trunc = indices(n_cols, max_cols, tail_cols)

    if colnames:
        rows = [" & ".join(colnames) + "\n"]
    else:
        rows = []

    for i in row_idx:
        entries = []
        for j in col_idx:
            entries.append(format_entry(arr[i, j]))

        if col_trunc:
            # Insert horizontal ellipsis before the tail columns
            insert_at = len(entries) - tail_cols
            entries.insert(insert_at, r"\ldots")

        rows.append(" & ".join(entries))

    if row_trunc:
        ellipsis_row = []
        displayed_cols = len(col_idx) + (1 if col_trunc else 0)
        if make_table:
            ellipsis_row = [r"$\vdots$"] * displayed_cols
        else:
            ellipsis_row = [r"\vdots"] * displayed_cols

        if col_trunc:
            ellipsis_col = len(ellipsis_row) - tail_cols - 1
            ellipsis_row[ellipsis_col] = r"$\ddots$" if make_table else r"\ddots" 

        # Insert ellipsis row before the tail rows
        insert_at = len(rows) - tail_rows
        rows.insert(insert_at, " & ".join(ellipsis_row))

    if make_table:
        body = " \\\\\n\\hline\n    ".join(rows)
        body = f"\\hline\n{body} \\\\\n\\hline\n"
    else:
        body = " \\\\\n    ".join(rows)

    # Check that every column has the same number of "&" because otherwise LaTeX will complain
    if True:
        expected_cols = None
        for r in rows:
            count = r.count("&")
            if expected_cols is None:
                expected_cols = count
            elif count != expected_cols:
                raise RuntimeError("Inconsistent column count in LaTeX matrix")

    
    if make_table:
        header_text = "|" + " c |" * max_cols
        return (
            f"\\begin{{tabular}}{{ {header_text} }}\n"
            f"    {body}\n"
            "\\end{tabular}"
        )
    else:
        return (
            "\\begin{bmatrix}\n"
            f"    {body}\n"
            "\\end{bmatrix}"
        )
    
def latex_table_to_array(latex_str):
    # Remove common LaTeX table wrappers
    latex_str = re.sub(r"\\begin\{.*?\}|\\end\{.*?\}", "", latex_str)
    latex_str = latex_str.replace(r"\hline", "")
    latex_str = latex_str.replace("$", "")

    # print(f"{latex_str=}")
    
    # Split into rows
    rows = re.split(r"\\\\", latex_str)

    # print(f"{rows=}")

    table = []
    
    for row in rows:
        row = row.strip()
        
        # Skip empty lines and \hline rows
        if not row:
            continue
        
        # Split into cells
        cells = row.split("&")

        # print(f"{cells=}")
        
        parsed_row = []
        for cell in cells:
            cell = cell.strip()
                        
            # Try to parse float
            try:
                val = float(cell)
            except ValueError: # Can't be parsed as a float
                val = np.nan
            
            parsed_row.append(val)
        
        table.append(parsed_row)
    
    return np.array(table, dtype=np.float64)

def square_matrix_tex(n : int, 
                      generator: Callable[[int, int], str],
                      start_ij: int = 1):
    rows = []
    for i in range(start_ij, n + start_ij):
        row = []
        for j in range(start_ij, n + start_ij):
            entry = generator(i, j)
            row.append(entry)
        rows.append(" & ".join(row))
    
    matrix_body = " \\\\\n".join(rows)
    
    latex = (
        "\\begin{bmatrix}\n"
        f"{matrix_body}\n"
        "\\end{bmatrix}\n"
    )
    return latex

def latex_vector(elements, orientation="column", bracket="bmatrix"):
    """
    Convert a list of strings into a LaTeX vector.

    Parameters:
        elements (list of str): The entries of the vector.
        orientation (str): "column" or "row".
        bracket (str): LaTeX matrix environment (e.g., "pmatrix", "bmatrix", "vmatrix").

    Returns:
        str: LaTeX string representing the vector.
    """
    if not elements:
        raise ValueError("elements list cannot be empty")

    if orientation == "col":
        orientation = "column"

    if orientation not in {"column", "row"}:
        raise ValueError("orientation must be 'column' or 'row'")

    if orientation == "column":
        body = " \\\\ ".join(elements)
    else:  # row
        body = " & ".join(elements)

    return f"\\begin{{{bracket}}}{body}\\end{{{bracket}}}"

def create_graph(
    func,
    x_range=None,
    y_range=None,
    x_label="x",
    y_label="y",
    domain=None,
    inverse_mode=False,
    inverse_func=None,   # maps y -> x (for inverse_mode mode)
    t_range=None,        # range in output space (y-space)
    n_points=1000,
    width = None,
    height = None,
    color=RED,
):
    # ---- defaults (avoid mutable args) ----
    x_range = x_range or [-5, 5, 1]
    y_range = y_range or [-5, 5, 1]

    if len(x_range) == 2:
        x_range = [*x_range, (x_range[1] - x_range[0]) * 0.1]
    if len(y_range) == 2:
        y_range = [*y_range, (y_range[1] - y_range[0]) * 0.1]

    domain = domain or x_range[0:2]
    if len(domain) == 2:
        step_size = (domain[1] - domain[0]) / n_points
        domain = [*domain, step_size]

    # ---- axes ----

    axes = Axes(
        x_range=x_range,
        y_range=y_range,
        x_length=width if width is not None else 6,
        y_length=height if height is not None else 6,
    ).add_coordinates()
    labels = axes.get_axis_labels(x_label=x_label, y_label=y_label)
    labels = axes.get_axis_labels(x_label=x_label, y_label=y_label)

    # ---- choose plotting mode ----
    if inverse_mode:
        if inverse_func is None:
            raise ValueError("inverse_mode=True requires inverse_func (mapping y -> x)")

        # default t_range from visible y-axis
        if t_range is None:
            t_range = y_range[0:2]
            # Apparently you can't control the step_size in t_range so never mind
            # step_size = (t_range[1] - t_range[0]) / n_points
            # t_range.append(step_size)

        def parametric(t):
            x = inverse_func(t)
            return axes.c2p(x, t)

        graph = ParametricFunction(
            parametric,
            t_range=t_range,
            color=color,
        )

    else:
        graph = axes.plot(
            func,
            x_range=domain,
            color=color,
        )

    return VGroup(axes, labels, graph)

