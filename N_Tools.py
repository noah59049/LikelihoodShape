def extract_table_grid(tex):
    glyphs = tex[0]

    vertical_lines = []
    horizontal_lines = []
    content = []

    # --- Step 1: classify glyphs ---
    for i, g in enumerate(glyphs):
        w, h = g.width, g.height

        if h > 5 * w:  # vertical line
            vertical_lines.append((i, g))
        elif w > 5 * h:  # horizontal line
            horizontal_lines.append((i, g))
        else:
            content.append((i, g))

    # print(f"{vertical_lines=}, {horizontal_lines=}, {content=}")

    # --- Step 2: get grid boundaries ---
    # Use centers of lines
    x_lines = sorted([g.get_center()[0] for _, g in vertical_lines])
    y_lines = sorted([g.get_center()[1] for _, g in horizontal_lines], reverse=True)

    # Deduplicate (important if LaTeX draws lines in segments)
    def dedup(vals, tol=1e-2):
        out = []
        for v in vals:
            if not any(abs(v - o) < tol for o in out):
                out.append(v)
        return sorted(out)

    x_lines = dedup(x_lines)
    y_lines = dedup(y_lines)[::-1]  # top → bottom

    # --- Step 3: build cells ---
    grid = {}

    for idx, g in content:
        x, y, _z = g.get_center()

        # find column: between vertical lines
        col = None
        for j in range(len(x_lines) - 1):
            if x_lines[j] < x < x_lines[j + 1]:
                col = j
                break

        # find row: between horizontal lines
        row = None
        for i in range(len(y_lines) - 1):
            if y_lines[i] > y > y_lines[i + 1]:
                row = i
                break

        if row is not None and col is not None:
            grid.setdefault((row, col), []).append(idx)
        else:
            pass
            # print((row, col))

    return grid

