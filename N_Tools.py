def logistic_regression(X, y, max_iter=100, tol=1e-6, add_intercept = False, return_stats = False):
    """
    Fits logistic regression using Newton-Raphson (IRLS).

    Parameters:
        X : (n, p) design matrix (include column of 1s if you want intercept)
        y : (n,) binary response (0 or 1)
        max_iter : maximum number of iterations
        tol : convergence tolerance

    Returns:
        beta : (p,) estimated coefficients
    """

    n, p = X.shape

    if add_intercept:
        X = np.c_[np.ones(n), X]
        n, p = X.shape

    beta = np.zeros(p)


    for _ in range(max_iter):
        z = X @ beta
        p_hat = sigmoid(z)

        # Gradient: X^T (y - p)
        grad = X.T @ (y - p_hat)

        # Hessian: -X^T W X where W = diag(p(1-p))
        W = p_hat * (1 - p_hat)
        # Avoid forming full diagonal matrix
        X_weighted = X * W[:, np.newaxis]
        hessian = -(X.T @ X_weighted)

        # Newton step
        try:
            delta = np.linalg.solve(hessian, grad)
        except np.linalg.LinAlgError:
            # fallback if singular
            delta = np.linalg.pinv(hessian) @ grad

        beta_new = beta - delta

        # Convergence check
        if np.linalg.norm(beta_new - beta) < tol:
            beta = beta_new
            break

        beta = beta_new

    if not return_stats:
        return beta

    # --- Compute covariance matrix at optimum ---
    z = X @ beta
    p_hat = sigmoid(z)
    W = p_hat * (1 - p_hat)

    X_weighted = X * W[:, np.newaxis]
    fisher_info = X.T @ X_weighted  # = X^T W X

    try:
        cov = np.linalg.inv(fisher_info)
    except np.linalg.LinAlgError:
        cov = np.linalg.pinv(fisher_info)

    se = np.sqrt(np.diag(cov))

    return beta, cov, se

