def highlight_row(tex, row_idx, color=RED, opacity=0.3):
    glyphs = tex[0]  # important fix

    # --- classify horizontal lines ---
    horizontal_lines = []
    for g in glyphs:
        w, h = g.width, g.height
        if w > 3 * h:  # horizontal line
            horizontal_lines.append(g)

    # --- extract and deduplicate y positions ---
    y_lines = sorted([g.get_center()[1] for g in horizontal_lines], reverse=True)

    def dedup(vals, tol=1e-2):
        out = []
        for v in vals:
            if not any(abs(v - o) < tol for o in out):
                out.append(v)
        return out

    y_lines = dedup(y_lines)

    # --- safety check ---
    if row_idx >= len(y_lines) - 1:
        raise ValueError("Row index out of range")

    y_top = y_lines[row_idx]
    y_bottom = y_lines[row_idx + 1]

    # --- compute rectangle geometry ---
    height = y_top - y_bottom
    y_center = (y_top + y_bottom) / 2

    # full table width
    left = min(g.get_left()[0] for g in glyphs)
    right = max(g.get_right()[0] for g in glyphs)
    width = right - left
    x_center = (left + right) / 2

    rect = Rectangle(width=width, height=height)
    rect.move_to([x_center, y_center, 0])
    rect.set_fill(color, opacity=opacity)
    rect.set_stroke(width=0)

    return rect

def FlashAround(
    mobject,
    color=YELLOW,
    line_length=0.25,
    num_lines=6,
    point_density=0.15,  # higher = more points
    lag_ratio=0.002,
    run_time=1
):
    anims = []

    # Flatten to leaf submobjects (glyph-level)
    submobs = mobject.family_members_with_points()

    for sm in submobs:
        pts = sm.get_points()
        if len(pts) == 0:
            continue

        # Sample points along the outline
        step = max(1, int(1 / point_density))
        sampled = pts[::step]

        for p in sampled:
            anims.append(
                Flash(
                    p,
                    color=color,
                    line_length=line_length,
                    num_lines=num_lines,
                )
            )

    if not anims:
        return AnimationGroup()  # safe no-op

    return LaggedStart(*anims, lag_ratio=lag_ratio, run_time=run_time)

def _glyph_group(mobj, indices):
    """
    Robust glyph extraction for Text, Tex, MathTex, etc.
    """
    try:
        return VGroup(*[mobj[i] for i in indices])
    except Exception:
        return VGroup(*[mobj[0][i] for i in indices])


class TransformWithBoxes(Succession):
    def __init__(
        self,
        src,
        dst,
        *mappings,                 # same format as TransformByGlyphMap
        box_kwargs=None,
        create_boxes_anim=Create,
        remove_boxes_anim=FadeOut,
        run_time=1.0,
        **kwargs                  # passed to Succession
    ):
        if type(run_time) == list or type(run_time) == tuple:
            time1, time2, time3 = run_time
        else:
            time1, time2, time3 = run_time, run_time, run_time

        box_kwargs = box_kwargs or {"color": RED, "buff": 0.1}

        # Build boxes
        src_boxes = VGroup(*[
            SurroundingRectangle(
                _glyph_group(src, src_inds),
                **box_kwargs
            )
            for src_inds, _ in mappings
        ])

        dst_boxes = VGroup(*[
            SurroundingRectangle(
                _glyph_group(dst, dst_inds),
                **box_kwargs
            )
            for _, dst_inds in mappings
        ])

        # Core animation
        transform = TransformByGlyphMap(src, dst, *mappings, run_time = time2)
        box_transform = Transform(src_boxes, dst_boxes, run_time = time2)
        transforms = AnimationGroup(transform, box_transform)

        # Full animation sequence
        animations = [
            create_boxes_anim(src_boxes, run_time = time1),
            transforms,
            remove_boxes_anim(src_boxes, run_time = time3),
        ]

        super().__init__(*animations, **kwargs)

def compute_z_range(
    z_func,
    x_range,
    y_range,
    samples=50,
    padding=0.1,
):
    print("Beginning to compute z range")
    t0 = time.time()
    x_min, x_max = x_range
    y_min, y_max = y_range

    xs = np.linspace(x_min, x_max, samples)
    ys = np.linspace(y_min, y_max, samples)

    Z = [
        z_func(x, y)
        for x in xs
        for y in ys
    ]

    z_min = min(Z)
    z_max = max(Z)

    span = z_max - z_min
    if span == 0:
        print("We artificially set span to 1")
        span = 1.0

    t1 = time.time()
    print(f"Finished compute_z_range, elapsed time = {t1-t0}")
    return (
        z_min - padding * span,
        z_max + padding * span,
        span / 4,  # tick step (optional heuristic)
    )

def surface_from_function(
    z_func,
    axes,
    x_range,
    y_range,
    resolution=32,
    color=BLUE_D,
):
    """
    Create a Manim Surface from a scalar function z(x, y).

    Parameters
    ----------
    z_func : callable
        Function z(x, y) -> float
    x_range : tuple
        (x_min, x_max)
    y_range : tuple
        (y_min, y_max)
    resolution : int
        Number of grid subdivisions per axis
    color : Manim color

    Returns
    -------
    Surface
    """

    x_min, x_max = x_range
    y_min, y_max = y_range

    def param_func(u, v):
        x = interpolate(x_min, x_max, u)
        y = interpolate(y_min, y_max, v)
        z = z_func(x, y)
        return axes.c2p(x, y, z)

    surface = Surface(
        param_func,
        u_range=(0, 1),
        v_range=(0, 1),
        resolution=(resolution, resolution),
        fill_color=color,
        fill_opacity=0.8,
        checkerboard_colors=[color, color],
    )

    return surface

def loglik_generator(X, y, add_intercept = True):
    def loglik(beta_hat0, beta_hat1):
        beta_hat = beta_hat0, beta_hat1
        beta_hat = np.array(beta_hat)
        result = log_likelihood(X, y, beta_hat, add_intercept=add_intercept)
        return result
    return loglik

def lik_generator(X, y, add_intercept = True):
    loglik = loglik_generator(X, y, add_intercept = add_intercept)
    def lik(beta_hat0, beta_hat1):
        return np.exp(loglik(beta_hat0, beta_hat1))
    return lik

def lik_scaled_generator(X, y, add_intercept = True):
    beta = logistic_regression(X, y, add_intercept = add_intercept, return_stats = False)
    loglik = loglik_generator(X, y)
    mle_loglik = log_likelihood(X, y, beta, add_intercept=add_intercept)
    def lik_scaled(beta_hat0, beta_hat1):
        return np.exp(loglik(beta_hat0, beta_hat1) - mle_loglik)
    return lik_scaled

